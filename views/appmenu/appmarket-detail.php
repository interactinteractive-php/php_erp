                '<i class="icon-cross3"></i>'+
            '</label></div>'+
            '<input id="appmarket-file-upload" type="file"/></div>'
        );
        $dialog.dialog({
          cache: false,
          resizable: true,
          bgiframe: true,
          autoOpen: false,
          title: "Logo",
          width: 400,
          height: "auto",
          modal: true,
          open: function () {
          },
          close: function () {
            $dialog.empty().dialog("destroy").remove();
